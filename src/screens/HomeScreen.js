// „Éõ„Éº„É†ÁîªÈù¢ - InstagramÊäïÁ®øÁîüÊàê„ÅÆ„É°„Ç§„É≥Ê©üËÉΩ
import React, { useState, useEffect } from 'react';
import {
  View,
  Text,
  ScrollView,
  StyleSheet,
  TouchableOpacity,
  Image,
  TextInput,
  Alert,
  ActivityIndicator,
  Platform
} from 'react-native';
import * as ImagePicker from 'expo-image-picker';
import * as Sharing from 'expo-sharing';
import * as Clipboard from 'expo-clipboard';
import * as Haptics from 'expo-haptics';
import { useTranslation } from 'react-i18next';

// „Çµ„Éº„Éì„Çπ„Å®„É¶„Éº„ÉÜ„Ç£„É™„ÉÜ„Ç£„ÅÆ„Ç§„É≥„Éù„Éº„Éà
import { processImage } from '../services/api';
import { preprocessImage, isImageSizeValid } from '../utils/imageUtils';
import { saveSettings, getSettings } from '../services/storage';
import { TEXT_TONES, TEXT_STYLES, HASHTAG_AMOUNTS, LANGUAGES, IMAGE_STYLES } from '../constants';

// „Ç´„Çπ„Çø„É†„Ç≥„É≥„Éù„Éº„Éç„É≥„Éà
import { CustomPicker } from '../components/CustomPicker';
import { InstagramHeader } from '../components/InstagramHeader';
import { InstagramButton } from '../components/InstagramButton';
import { InstagramCard } from '../components/InstagramCard';
import { LanguageSwitcher } from '../components/LanguageSwitcher';
import { useThemeColors } from '../constants/colors';
import { Ionicons } from '@expo/vector-icons';

const HomeScreen = () => {
  // Â§öË®ÄË™ûÂØæÂøú
  const { t } = useTranslation();

  // „ÉÜ„Éº„Éû„Ç´„É©„Éº„ÅÆÂèñÂæó
  const colors = useThemeColors();

  // Áä∂ÊÖãÁÆ°ÁêÜ
  const [selectedImage, setSelectedImage] = useState(null); // ÂÖÉ„ÅÆÈ´òËß£ÂÉèÂ∫¶ÁîªÂÉèÔºàË°®Á§∫„Éª„Ç∑„Çß„Ç¢„Éª‰øùÂ≠ò„Å´‰ΩøÁî®Ôºâ
  const [generatedCaption, setGeneratedCaption] = useState('');
  const [generatedText, setGeneratedText] = useState(''); // ÊäïÁ®øÊñá„ÅÆ„Åø
  const [generatedHashtags, setGeneratedHashtags] = useState(''); // „Éè„ÉÉ„Ç∑„É•„Çø„Ç∞„ÅÆ„Åø
  const [requiredKeyword, setRequiredKeyword] = useState('');
  const [selectedTone, setSelectedTone] = useState('serious');
  const [selectedStyle, setSelectedStyle] = useState('neutral');
  const [hashtagAmount, setHashtagAmount] = useState('normal');
  const [language, setLanguage] = useState('japanese');
  const [imageStyle, setImageStyle] = useState('original');
  const [loading, setLoading] = useState(false);
  const [loadingMessage, setLoadingMessage] = useState('');

  // ÂàùÊúüË®≠ÂÆö„ÅÆË™≠„ÅøËæº„Åø
  useEffect(() => {
    loadInitialSettings();
  }, []);

  /**
   * ÂàùÊúüË®≠ÂÆö„ÇíË™≠„ÅøËæº„ÇÄ
   */
  const loadInitialSettings = async () => {
    try {
      const settings = await getSettings();
      setSelectedTone(settings.defaultTone || 'serious');
      setSelectedStyle(settings.defaultStyle || 'neutral');
      setHashtagAmount(settings.defaultHashtagAmount || 'normal');
      setLanguage(settings.defaultLanguage || 'japanese');
      setImageStyle(settings.defaultImageStyle || 'original');
    } catch (error) {
      console.error('Ë®≠ÂÆö„ÅÆË™≠„ÅøËæº„Åø„Å´Â§±Êïó:', error);
    }
  };

  /**
   * Ë®≠ÂÆö„Çí‰øùÂ≠ò„Åô„ÇãÔºàPickerÂ§âÊõ¥ÊôÇ„Å´Âç≥Â∫ß„Å´‰øùÂ≠òÔºâ
   */
  const updateAndSaveSettings = async (key, value) => {
    try {
      const currentSettings = await getSettings();
      const newSettings = {
        ...currentSettings,
        [key]: value
      };
      await saveSettings(newSettings);
      console.log(`Ë®≠ÂÆö„Çí‰øùÂ≠ò„Åó„Åæ„Åó„Åü: ${key} = ${value}`);
    } catch (error) {
      console.error('Ë®≠ÂÆö„ÅÆ‰øùÂ≠ò„Å´Â§±Êïó:', error);
    }
  };

  /**
   * ÁîªÂÉèÈÅ∏Êäû„ÉÄ„Ç§„Ç¢„É≠„Ç∞„ÇíË°®Á§∫
   */
  const selectImage = () => {
    Alert.alert(
      t('imagePicker.selectPhoto'),
      t('imagePicker.selectMethod'),
      [
        { text: t('imagePicker.takePhoto'), onPress: openCamera },
        { text: t('imagePicker.chooseFromGallery'), onPress: openGallery },
        { text: t('imagePicker.cancel'), style: 'cancel' }
      ]
    );
  };

  /**
   * „Ç´„É°„É©„ÇíÈñã„ÅÑ„Å¶ÂÜôÁúü„ÇíÊíÆÂΩ±
   */
  const openCamera = async () => {
    try {
      // „Ç´„É°„É©Ê®©Èôê„ÅÆÁ¢∫Ë™ç
      const { status } = await ImagePicker.requestCameraPermissionsAsync();
      if (status !== 'granted') {
        Alert.alert(t('alerts.error'), t('alerts.cameraPermission'));
        return;
      }

      const result = await ImagePicker.launchCameraAsync({
        mediaTypes: ImagePicker.MediaTypeOptions.Images,
        allowsEditing: true,
        aspect: [1, 1],
        quality: 0.8
      });

      if (!result.canceled) {
        // Ëß¶Ë¶ö„Éï„Ç£„Éº„Éâ„Éê„ÉÉ„ÇØ
        await Haptics.impactAsync(Haptics.ImpactFeedbackStyle.Light);

        setSelectedImage({
          uri: result.assets[0].uri
        });

        // ÁµêÊûú„Çí„ÇØ„É™„Ç¢
        setGeneratedCaption('');
        setGeneratedText('');
        setGeneratedHashtags('');
      }
    } catch (error) {
      console.error('„Ç´„É°„É©„Ç®„É©„Éº:', error);
      Alert.alert(t('alerts.error'), t('alerts.cameraError'));
    }
  };

  /**
   * „ÇÆ„É£„É©„É™„Éº„Åã„ÇâÂÜôÁúü„ÇíÈÅ∏Êäû
   */
  const openGallery = async () => {
    try {
      // „ÇÆ„É£„É©„É™„ÉºÊ®©Èôê„ÅÆÁ¢∫Ë™ç
      const { status } = await ImagePicker.requestMediaLibraryPermissionsAsync();
      if (status !== 'granted') {
        Alert.alert(t('alerts.error'), t('alerts.galleryPermission'));
        return;
      }

      const result = await ImagePicker.launchImageLibraryAsync({
        mediaTypes: ImagePicker.MediaTypeOptions.Images,
        allowsEditing: true,
        aspect: [1, 1],
        quality: 0.8
      });

      if (!result.canceled) {
        // Ëß¶Ë¶ö„Éï„Ç£„Éº„Éâ„Éê„ÉÉ„ÇØ
        await Haptics.impactAsync(Haptics.ImpactFeedbackStyle.Light);

        setSelectedImage({
          uri: result.assets[0].uri
        });

        // ÁµêÊûú„Çí„ÇØ„É™„Ç¢
        setGeneratedCaption('');
        setGeneratedText('');
        setGeneratedHashtags('');
      }
    } catch (error) {
      console.error('„ÇÆ„É£„É©„É™„Éº„Ç®„É©„Éº:', error);
      Alert.alert(t('alerts.error'), t('alerts.galleryError'));
    }
  };

  /**
   * ÁîªÂÉè„ÇíÂá¶ÁêÜ„Åó„Å¶„Ç≠„É£„Éó„Ç∑„Éß„É≥„ÇíÁîüÊàê
   */
  const handleProcess = async () => {
    if (!selectedImage) {
      Alert.alert(t('alerts.error'), t('alerts.selectImage'));
      return;
    }

    setLoading(true);
    setLoadingMessage(t('loading.processing'));

    try {
      // ÁîªÂÉè„ÅÆÂâçÂá¶ÁêÜÔºàÊ≠£ÊñπÂΩ¢„Å´„Éà„É™„Éü„É≥„Ç∞ + ÂúßÁ∏ÆÔºâ
      // ‚ö†Ô∏è „Åì„ÅÆ‰ΩéÁîªË≥™Áâà„ÅØGemini API„Å∏„ÅÆÈÄÅ‰ø°„ÅÆ„Åø„Å´‰ΩøÁî®ÔºàË°®Á§∫„Éª‰øùÂ≠ò„Å´„ÅØ‰Ωø„Çè„Å™„ÅÑÔºâ
      setLoadingMessage(t('loading.optimizing'));
      const processedImageData = await preprocessImage(selectedImage.uri, 1080, 0.8);

      // ÁîªÂÉè„Çµ„Ç§„Ç∫„ÅÆÊ§úË®ºÔºà4MBÂà∂ÈôêÔºâ
      if (!isImageSizeValid(processedImageData.base64, 4)) {
        throw new Error(t('alerts.imageSizeError'));
      }

      // API„Å´ÈÄÅ‰ø°Ôºà„ÉÜ„Ç≠„Çπ„ÉàÁîüÊàê„ÅÆ„Åø„ÄÅÁîªÂÉè„ÅØËøî„Å£„Å¶„Åì„Å™„ÅÑÔºâ
      setLoadingMessage(t('loading.generating'));
      const result = await processImage({
        image: processedImageData.base64,
        requiredKeyword,
        tone: selectedTone,
        style: selectedStyle,
        hashtagAmount,
        language,
        imageStyle
      });

      // ÁµêÊûú„ÇíË®≠ÂÆö
      setGeneratedCaption(result.caption);
      setGeneratedText(result.generatedText);
      setGeneratedHashtags(result.hashtags);

      // ÊàêÂäü„Éï„Ç£„Éº„Éâ„Éê„ÉÉ„ÇØ
      await Haptics.notificationAsync(Haptics.NotificationFeedbackType.Success);
      Alert.alert(t('alerts.success'), t('alerts.generated'));

    } catch (error) {
      console.error('Âá¶ÁêÜ„Ç®„É©„Éº:', error);
      await Haptics.notificationAsync(Haptics.NotificationFeedbackType.Error);
      Alert.alert(t('alerts.error'), error.message || t('alerts.processingError'));
    } finally {
      setLoading(false);
      setLoadingMessage('');
    }
  };

  /**
   * ÂàùÊúüÁä∂ÊÖã„Å´„É™„Çª„ÉÉ„ÉàÔºàÂà•„ÅÆÁîªÂÉè„Åß„ÇÑ„Çä„Å™„Åä„ÅôÔºâ
   */
  const resetToInitialState = () => {
    setSelectedImage(null);
    setGeneratedCaption('');
    setGeneratedText('');
    setGeneratedHashtags('');
    // Ë®≠ÂÆö„ÅØ„Åù„ÅÆ„Åæ„Åæ‰øùÊåÅ
  };

  /**
   * „Ç≠„É£„Éó„Ç∑„Éß„É≥„Çí„ÇØ„É™„ÉÉ„Éó„Éú„Éº„Éâ„Å´„Ç≥„Éî„Éº
   */
  const copyToClipboard = async () => {
    try {
      await Clipboard.setStringAsync(generatedCaption);
      await Haptics.notificationAsync(Haptics.NotificationFeedbackType.Success);
      Alert.alert(t('alerts.success'), t('alerts.copied'));
    } catch (error) {
      console.error('„Ç≥„Éî„Éº„Ç®„É©„Éº:', error);
      Alert.alert(t('alerts.error'), t('alerts.copyError'));
    }
  };

  /**
   * ÁîªÂÉè„Å®„Ç≠„É£„Éó„Ç∑„Éß„É≥„Çí„Ç∑„Çß„Ç¢
   * ‚ö†Ô∏è ÂÖÉ„ÅÆÈ´òËß£ÂÉèÂ∫¶ÁîªÂÉè„Çí‰ΩøÁî®Ôºà‰ΩéÁîªË≥™Áâà„ÅØ‰Ωø„Çè„Å™„ÅÑÔºâ
   * ‚ú® „Ç∑„Çß„Ç¢ÊôÇ„Å´ÊäïÁ®øÊñá„ÇíËá™Âãï„Åß„ÇØ„É™„ÉÉ„Éó„Éú„Éº„Éâ„Å´„Ç≥„Éî„Éº
   * üì± „Ç¢„É©„Éº„ÉàË°®Á§∫ ‚Üí OK„Éú„Çø„É≥ ‚Üí „Ç∑„Çß„Ç¢„Ç¶„Ç£„É≥„Éâ„Ç¶„ÅÆÈ†ÜÂ∫è
   */
  const shareContent = async () => {
    try {
      const isAvailable = await Sharing.isAvailableAsync();
      if (isAvailable && selectedImage?.uri) {
        // ÊäïÁ®øÊñá„ÇíËá™Âãï„Åß„Ç≥„Éî„ÉºÔºàInstagram„Å´Ë≤º„Çä‰ªò„Åë„Çã„Åü„ÇÅÔºâ
        await Clipboard.setStringAsync(generatedCaption);

        // „Ç¢„É©„Éº„Éà„ÇíË°®Á§∫„Åó„Å¶„ÄÅOK„ÇíÊäº„Åó„Åü„Çâ„Ç∑„Çß„Ç¢„Ç¶„Ç£„É≥„Éâ„Ç¶„ÇíÈñã„Åè
        Alert.alert(
          t('alerts.shareReady'),
          t('alerts.shareMessage'),
          [
            {
              text: t('alerts.ok'),
              onPress: async () => {
                try {
                  // OK„ÇíÊäº„Åó„Åü„Çâ„Ç∑„Çß„Ç¢„Ç¶„Ç£„É≥„Éâ„Ç¶„ÇíÈñã„Åè
                  await Sharing.shareAsync(selectedImage.uri, {
                    mimeType: 'image/jpeg',
                    dialogTitle: t('header.title')
                  });
                  await Haptics.impactAsync(Haptics.ImpactFeedbackStyle.Light);
                } catch (shareError) {
                  console.error('„Ç∑„Çß„Ç¢„Ç®„É©„Éº:', shareError);
                  Alert.alert(t('alerts.error'), t('alerts.shareError'));
                }
              }
            }
          ]
        );
      } else {
        Alert.alert(t('alerts.error'), t('alerts.shareUnavailable'));
      }
    } catch (error) {
      console.error('„Ç≥„Éî„Éº„Ç®„É©„Éº:', error);
      Alert.alert(t('alerts.error'), t('alerts.copyError'));
    }
  };

  return (
    <ScrollView style={[styles.container, { backgroundColor: colors.background }]} contentContainerStyle={styles.contentContainer}>
      {/* „É≠„Éº„Éá„Ç£„É≥„Ç∞„Ç™„Éº„Éê„Éº„É¨„Ç§ */}
      {loading && (
        <View style={styles.loadingOverlay}>
          <ActivityIndicator size="large" color={colors.primary} />
          <Text style={styles.loadingText}>{loadingMessage}</Text>
        </View>
      )}

      {/* InstagramÈ¢®„Éò„ÉÉ„ÉÄ„ÉºÔºà„Çπ„ÇØ„É≠„Éº„É´ÂèØËÉΩÔºâ */}
      <InstagramHeader />

      {/* Ë®ÄË™ûÂàá„ÇäÊõø„Åà„Ç≥„É≥„Éù„Éº„Éç„É≥„Éà */}
      <LanguageSwitcher />

        {/* ÁîªÂÉèÈÅ∏Êäû„Ç®„É™„Ç¢ */}
        <InstagramCard>
          <TouchableOpacity
            style={styles.imageSelector}
            onPress={selectImage}
            activeOpacity={0.7}
          >
            {selectedImage ? (
              <Image source={{ uri: selectedImage.uri }} style={styles.selectedImage} />
            ) : (
              <View style={styles.placeholderContainer}>
                <Ionicons name="camera" size={60} color={colors.textSecondary} style={styles.placeholderIcon} />
                <Text style={[styles.placeholderText, { color: colors.textSecondary }]}>{t('imagePicker.placeholder')}</Text>
              </View>
            )}
          </TouchableOpacity>
        </InstagramCard>

        {/* Ë®≠ÂÆö„Éï„Ç©„Éº„É† */}
        <InstagramCard>
          <View style={styles.form}>
        {/* ÂøÖÈ†à„Ç≠„Éº„ÉØ„Éº„Éâ */}
        <View style={styles.inputGroup}>
          <Text style={styles.label}>{t('form.requiredKeyword')}</Text>
          <TextInput
            style={styles.textInput}
            placeholder={t('form.requiredKeywordPlaceholder')}
            value={requiredKeyword}
            onChangeText={setRequiredKeyword}
            maxLength={20}
            editable={!loading}
          />
        </View>

        {/* ÊñáÁ´†„ÅÆ„Éà„Éº„É≥ */}
        <View style={styles.inputGroup}>
          <CustomPicker
            label={t('form.tone')}
            selectedValue={selectedTone}
            onValueChange={(value) => {
              setSelectedTone(value);
              updateAndSaveSettings('defaultTone', value);
            }}
            options={TEXT_TONES}
            disabled={loading}
          />
        </View>

        {/* ÊñáÁ´†„ÅÆ„Çπ„Çø„Ç§„É´ */}
        <View style={styles.inputGroup}>
          <CustomPicker
            label={t('form.style')}
            selectedValue={selectedStyle}
            onValueChange={(value) => {
              setSelectedStyle(value);
              updateAndSaveSettings('defaultStyle', value);
            }}
            options={TEXT_STYLES}
            disabled={loading}
          />
        </View>

        {/* „Éè„ÉÉ„Ç∑„É•„Çø„Ç∞„ÅÆÈáè */}
        <View style={styles.inputGroup}>
          <CustomPicker
            label={t('form.hashtagAmount')}
            selectedValue={hashtagAmount}
            onValueChange={(value) => {
              setHashtagAmount(value);
              updateAndSaveSettings('defaultHashtagAmount', value);
            }}
            options={HASHTAG_AMOUNTS}
            disabled={loading}
          />
        </View>

            {/* Ë®ÄË™ûË®≠ÂÆöÔºàAPIÁîüÊàêË®ÄË™ûÁî® - UI„É≠„Ç±„Éº„É´„Å®„ÅØÁã¨Á´ãÔºâ */}
            <View style={styles.inputGroup}>
              <CustomPicker
                label={t('form.language')}
                selectedValue={language}
                onValueChange={(value) => {
                  setLanguage(value);
                  updateAndSaveSettings('defaultLanguage', value);
                }}
                options={LANGUAGES}
                disabled={loading}
              />
            </View>
          </View>
        </InstagramCard>

        {/* ÁîüÊàê„Éú„Çø„É≥ */}
        <View style={styles.buttonContainer}>
          <InstagramButton
            title={loading ? t('buttons.generating') : t('buttons.generate')}
            icon={<Ionicons name="sparkles" size={20} color="#fff" />}
            onPress={handleProcess}
            disabled={loading || !selectedImage}
            accessibilityLabel={t('accessibility.generate')}
            accessibilityHint={t('accessibility.generateHint')}
          />
        </View>

        {/* ÁµêÊûúË°®Á§∫„Ç®„É™„Ç¢ÔºàÊäïÁ®øÊñá„Å®„Éè„ÉÉ„Ç∑„É•„Çø„Ç∞„ÅÆ„ÅøË°®Á§∫Ôºâ */}
        {generatedCaption && (
          <>
            <InstagramCard>
              <Text style={[styles.resultTitle, { color: colors.textPrimary }]}>{t('results.title')}</Text>

              {/* ÁîüÊàê„Åï„Çå„Åü„Ç≠„É£„Éó„Ç∑„Éß„É≥ */}
              <View style={styles.captionContainer}>
                <Text style={[styles.captionText, { color: colors.textPrimary }]}>{generatedCaption}</Text>

                {/* „Ç¢„ÇØ„Ç∑„Éß„É≥„Éú„Çø„É≥ */}
                <View style={styles.actionButtons}>
                  <TouchableOpacity
                    style={styles.actionButton}
                    onPress={copyToClipboard}
                    activeOpacity={0.7}
                  >
                    <Text style={styles.actionButtonText}>{t('buttons.copy')}</Text>
                  </TouchableOpacity>

                  <TouchableOpacity
                    style={styles.actionButton}
                    onPress={shareContent}
                    activeOpacity={0.7}
                  >
                    <Text style={styles.actionButtonText}>{t('buttons.share')}</Text>
                  </TouchableOpacity>
                </View>
              </View>
            </InstagramCard>

            {/* Âà•„ÅÆÁîªÂÉè„Åß„ÇÑ„Çä„Å™„Åä„Åô„Éú„Çø„É≥ */}
            <View style={styles.buttonContainer}>
              <InstagramButton
                title={t('buttons.retry')}
                icon={<Ionicons name="refresh" size={20} color="#fff" />}
                onPress={resetToInitialState}
                disabled={loading}
                accessibilityLabel={t('accessibility.retry')}
                accessibilityHint={t('accessibility.retryHint')}
              />
            </View>
          </>
        )}

      {/* ‰∏ãÈÉ®„ÅÆ‰ΩôÁôΩ */}
      <View style={styles.bottomSpacer} />
    </ScrollView>
  );
};

